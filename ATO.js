
var paciente = document.querySelector('#primeiro-paciente');

var valorPeso = paciente.querySelector('.info-peso')
var peso = parseInt(valorPeso.textContent); // <--- 10
//console.log(peso)


var valorAltura = paciente.querySelector('.info-altura')
altura = parseInt(valorAltura.textContent) // <-- 8
//console.log(altura)


var valorGordura = paciente.querySelector('.info-gordura')
gordura = parseInt (valorGordura.textContent) // <-- 7
//console.log(gordura)

var valorMedia = (peso + altura + gordura) / 3
console.log(valorMedia)

var subMedia = paciente.querySelector('.info-imc')
subMedia.textContent = valorMedia.toFixed(1)



